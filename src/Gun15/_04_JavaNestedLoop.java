package Gun15;

public class _04_JavaNestedLoop {
    public static void main(String[] args) {



        // aşağıdaki görüntüyü veren programı yazdırın
        //*****
        //****
        //***
        //**
        //*


        for (int satir=5;   satir>0;   satir--) {

            for (int j=0;  j<satir;  j++)// yıldız adeti burdan belirleniyor
                System.out.print("*");


            System.out.println();


        }
    }
}
