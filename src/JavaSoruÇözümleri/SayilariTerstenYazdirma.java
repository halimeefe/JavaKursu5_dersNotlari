package JavaSoruÇözümleri;

public class SayilariTerstenYazdirma {
    public static void main(String[] args) {



        // 100 den 1 e kadar olan tek sayıları tersten yazdırın

        int i=100;
        for (i=100;i>=1;i--){
            System.out.println("100 den 1 e tersten sayılar:"+i);
        }
    }
}
